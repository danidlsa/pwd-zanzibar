# Normalize fieldwork district names to match OCGS format (underscores → spaces)
normalize_district <- function(x) gsub("_", " ", x)

# Split space-separated multi-value field into vector
split_field <- function(x) {
  if (is.null(x) || is.na(x) || x == "" || x == "null") return(character(0))
  trimws(strsplit(x, " ")[[1]])
}

# Translate multi-value field to readable labels
translate_multivalue <- function(x, label_map) {
  keys <- split_field(x)
  if (length(keys) == 0) return(character(0))
  labels <- label_map[keys]
  matched <- labels[!is.na(labels)]
  unmatched <- keys[!(keys %in% names(label_map))]
  # For unmatched keys, pretty-print: replace _ and - with spaces, title case
  pretty_unmatched <- tools::toTitleCase(gsub("[_-]", " ", unmatched))
  as.character(c(matched, pretty_unmatched))
}
